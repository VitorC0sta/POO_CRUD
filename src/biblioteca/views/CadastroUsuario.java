    private void createNewUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createNewUserButtonActionPerformed
        if(inputTextName.getText().isEmpty() || inputTextRA.getText().isEmpty()) {
            JOptionPane.showMessageDialog(CadastroUsuario.this, "Os campos est√£o vazios.");
        } else {
            String userType = (String) selectUserType.getSelectedItem();
            String userName = (String) inputTextName.getText();
            String userRA = (String) inputTextRA.getText();
            
            Usuario user = new Usuario(userName,userRA , userType, true);
            
            try {
                userController.create(user);
                clearInputsFn();
                
            } catch (Exception ex){
                JOptionPane.showMessageDialog(
                    CadastroUsuario.this,
                    ex.getMessage(),
                    "Algo deu errado, tente novamente !",
                    JOptionPane.ERROR_MESSAGE
                );
            }
        }
    }//GEN-LAST:event_createNewUserButtonActionPerformed
    
    
    private void clearInputsFn() {
        inputTextName.setText("");
        inputTextRA.setText("");
        selectUserType.setSelectedIndex(0);
    }
